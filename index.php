                    <h3>カード払い</h3>
                    <p>
                        トロントはカード社会なので基本現金を持ち歩かなくても困らない。電車やバスもクレジットカードで乗ることができるが、PRESTOカード（後述）を使うことで割引特典が得られる。私が現金を使うのは家賃の支払い（家主の脱税疑惑はあるが、現金払いは一般的）、コインランドリーくらい。
