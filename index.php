                                付け合わせには、クランベリーソースを用いる。ターキーの肉自体はスーパーでも一年中売っている。</dd>

                            <dt>モントリオール風スモークミート</dt>
                            <dd>牛肉の胸肉を塩漬けにしてスパイスで漬け込んでから燻製し、最後に蒸したもの。ニューヨークでも似ているスタイルのパストラミが有名。</dd>

                            <dt>ロブスター</dt>
                            <dd>東のプリンス・エドワード島が産地として有名だがトロントでもたべられる。ニューヨークでも食べられるロブスターロールはトロントで食べた方が美味しい。</dd>

                            <dt>ヨークシャープディングやフィッシュ&チップス</dt>
                            <dd>イギリス文化が残っているのでこれらもよく食べられるらしい。このプディングはいわゆる「プリン」ではなく、ふわふわでもっちりとしたシュークリームの皮のようなもの。
