                            <dt>コーヒークリスプ</dt>
                            <dd>カナダで市販されているチョコレートバー。バニラウエハースと泡立ったコーヒー風味のソフトキャンディが交互に重なり、外側はミルクチョコレートで覆われている。
                                個人的にあまりチョコレート菓子で好きなものはないのだが、これは結構好き。甘いけれど。
                            </dd>

                            <dt>ストリートフード</dt>
                            <dd>カナダ料理というわけではないが、人が集まるところにフードトラックよくいる。
                                ホットドッグを始め、ケバブ、ファラフェル、シャワルマや他のハラールフードなど世界中の料理がストリートフードとして非常に人気。ニューヨークでも見られる。</dd>
