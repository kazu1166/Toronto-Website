                    <h3>トイレ</h3>
                    <p>
                        個人的にかなり驚いたのは公共トイレがほとんどないこと。よっぽど大きい駅でないとトイレはないし、コンビニやスーパーでは借りることはできない。レストランやファストフード店で借りることができるが、鍵を借りないといけない場所もあるので注意。
                        カナダではマックやどのコンビニより店舗数が多い国民的カフェ、ティムホートンズ（Tim Hortons）は価格の面でもトイレの面でも市民の強い味方である。
