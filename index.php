                    <h3>気候</h3>
                    <p>
                        トロントはその緯度とオンタリオ湖に面することから、カナダの中では温暖かつ湿度が高い気候であるとされるが、西海岸沿いに比べると寒い。四季がはっきりしているが、日々の気温に差があり、冬の寒い時期は特に気温差が大きくなる傾向にある。
